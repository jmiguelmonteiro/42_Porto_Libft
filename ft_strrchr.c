#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		len;
	char	*str;

	str = (char *)s;
	len = ft_strlen(str);
	if (c == '\0')
		return (&str[len]);
	while (len > 0)
	{
		if (str[len - 1] == c)
			return (&str[len - 1]);
		len--;
	}
	return (NULL);
}
